###Перейдите в цикле по списку ["Вася", "Маша", "Петя", "Валера", "Саша", "Даша"] пока не встретите имя "Валера". Когда найдете напишите "Валера нашелся". Подсказка: используйте метод list.pop() 

names=["Вася", "Маша", "Петя", "Валера", "Саша", "Даша"]
while names:
    print(names[-1])
    print(names)
    if names.pop() == 'Валера':
        print('Валера нашелся')
        break
